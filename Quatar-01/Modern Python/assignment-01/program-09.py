# - Write a program that calculates the volume
# of a cylinder using the formula .

import math

# Variables for radius (r) and height (h)
radius = 5  # Example radius in meters
height = 10  # Example height in meters

# Calculate the volume of the cylinder
volume = math.pi * radius**2 * height

# Display the volume
print(f"The volume of the cylinder is: {volume:.2f} cubic meters")
