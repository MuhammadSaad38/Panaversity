# - Write a program that calculates the BMI using
# height (in meters) and weight (in kilograms) variables.
# Variables for weight (in kilograms) and height (in meters)
weight = 70  # Example weight in kilograms
height = 1.75  # Example height in meters

# Calculate BMI
bmi = weight / (height ** 2)

# Display the BMI
print(f"The BMI is: {bmi:.2f}")
