def calculate_percentage(part, whole):
    # Step 1: Get the part and the whole
    part = float(part)
    whole = float(whole)
    
    # Step 2: Calculate the fraction by dividing part by whole
    fraction = part / whole
    
    # Step 3: Calculate the percentage by multiplying by 100
    percentage = fraction * 100
    
    # Step 4: Return the calculated percentage
    return percentage
