def surface_area_of_cube(side_length):
    # Step 1: Get the side length of the cube
    side_length = float(side_length)
    
    # Step 2: Calculate the surface area using the formula 6 * side_length^2
    surface_area = 6 * side_length ** 2
    
    # Step 3: Return the calculated surface area
    return surface_area
